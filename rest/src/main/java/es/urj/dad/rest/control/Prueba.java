package es.urj.dad.rest.control;

public class Prueba {

    private String a;
    private int b;
    
    public Prueba(String a, int b) {
        this.a = a;
        this.b = b;
    }
    public Prueba() {
    }
    public String getA() {
        return a;
    }
    public void setA(String a) {
        this.a = a;
    }
    public int getB() {
        return b;
    }
    public void setB(int b) {
        this.b = b;
    }

    
    
}
